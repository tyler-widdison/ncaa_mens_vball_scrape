import json
import csv
import urllib.request

df_links = pd.read_csv('ncaa_m_links.csv')
urls_2020 = []
with open('ncaa_m_links.csv', 'r') as csvfile:
    reader = csv.reader(csvfile)
    for row in reader:
        urls_2020.append(row[3])


dfs = []
dfs2 = []
for u in urls_2020[1:]:   
    try:
        with urllib.request.urlopen(u) as url:
            data = json.loads(url.read().decode())
            match = data['meta']['description'].split('for ')[1]
            date = data['meta']['updatedTimestamp'].split(' ')[0]
            division = data['meta']['division']
            df_home_team = pd.DataFrame(data['teams'][0]['playerStats'])
            df_away_team = pd.DataFrame(data['teams'][1]['playerStats'])
            is_team_home_team = data['meta']['teams'][0]['homeTeam']
            is_team_home_team2 = data['meta']['teams'][1]['homeTeam']
            team = data['meta']['teams'][0]['shortName']
            opponent = data['meta']['teams'][1]['shortName']
            df = pd.DataFrame(df_home_team).assign(match=match, division=division, date=date, team=opponent, opponent=team, is_team_home_team=is_team_home_team2, u=u)
            df2 = pd.DataFrame(df_away_team).assign(match=match, division=division, date=date, team=team, opponent=opponent, is_team_home_team=is_team_home_team, u=u)
            dfs.append(df)
            dfs2.append(df2)
    except:
        continue

df_fin_home = pd.concat(dfs)
df_fin_away = pd.concat(dfs2)
df = pd.concat([df_fin_home, df_fin_away])
df.to_csv('men_2016-2020_box_scores.csv')

